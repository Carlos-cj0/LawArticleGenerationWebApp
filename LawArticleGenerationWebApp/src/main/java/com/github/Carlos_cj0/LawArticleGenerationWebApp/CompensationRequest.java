package com.github.Carlos_cj0.LawArticleGenerationWebApp;



public class CompensationRequest {
    private int marriageYears;
    private double dailyHouseworkHours;
    private double spouseIncome;

    public int getMarriageYears() {
        return marriageYears;
    }

    public void setMarriageYears(int marriageYears) {
        this.marriageYears = marriageYears;
    }

    public double getDailyHouseworkHours() {
        return dailyHouseworkHours;
    }

    public void setDailyHouseworkHours(double dailyHouseworkHours) {
        this.dailyHouseworkHours = dailyHouseworkHours;
    }

    public double getSpouseIncome() {
        return spouseIncome;
    }

    public void setSpouseIncome(double spouseIncome) {
        this.spouseIncome = spouseIncome;
    }
}